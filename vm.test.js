const startVM = require('./vm');

test('performs addition', () => {
  const program = [
    '0x01', '0x01', '0x10',
    '0x01', '0x02', '0x12',
    '0x03', '0x01', '0x02',
    '0x02', '0x01', '0x0e',
    '0xff',
    '0x00',
    '0x00', '0x00',
    '0xa1', '0x14',
    '0x0c', '0x00'
  ];

  const memory = [null, null, null, null, null, null, null, null, null, null, null, null, null, null, null, null];

  expect(startVM(memory, program)).toEqual([
    '0x01', '0x01', '0x10',
    '0x01', '0x02', '0x12',
    '0x03', '0x01', '0x02',
    '0x02', '0x01', '0x0e',
    '0xff', '0x00', 'ad',
    '14',   '0xa1', '0x14',
    '0x0c', '0x00'
  ])
});